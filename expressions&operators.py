# Expressions
# + operator
print(2+2)
print(type(2+2))

print(2.4+2.3)
print(type(2.4+2.3))

print(2.4+2)
print(type(2.4+2))

# - operator
print(4-2)
print(type(4-2))

print(2.4-2.1)
print(type(2.4-2.1))

# * operator
print(4*2)
print(type(4*2))

print(2.4*2.1)
print(type(2.4*2.1))

print(2.4*2)
print(type(2.4*2))

# / operator
print(3/2)
print(type(3/2))

print(3/1.5)
print(type(3/1.5))

# // int division operator
print(5//2)
print(type(5//2))

print(5//2.5)
print(type(5//2.5))

print(4.2//5)
print(type(4.2//5))

# % mod operator
print(5%2)
print(type(5%2))

print(2.5%2)
print(type(2.5%2))

# ** mod operator
print(2**3)
print(type(2**3))

print(2.4**2)
print(type(2.4**2))

print(5**-1)
print(type(5**-1))